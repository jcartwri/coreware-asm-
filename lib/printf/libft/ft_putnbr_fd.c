#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	p;

	p = 0;
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		ft_putnbr_fd(147483648, fd);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		p = (n % 10) + 48;
		write(fd, &p, 1);
	}
	else
	{
		p = n + 48;
		write(fd, &p, 1);
	}
}
